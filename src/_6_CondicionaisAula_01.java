public class _6_CondicionaisAula_01 {
    public static void main(String[] args) {

        //sout + tab

        // A condição sempre vai retornar um valor:
        // Verdadeio ou Falso, não existe outra opção.
        boolean condicao = true ;

        if (condicao) {
            System.out.println("Dentro do IF");
        } else {
            System.out.println("Dentro do ELSE");
        }
        System.out.println("Fora do IF e Fora do ELSE");

    }

}
